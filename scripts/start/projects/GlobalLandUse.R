write_global_ghg_prices <- function() {
  base_prices <- read.magpie("modules/56_ghg_policy/input/f56_pollutant_prices.cs3")
  base_prices <- collapseNames(base_prices[, , getNames(base_prices, dim = 2)[1]])
  base_prices[, , ] <- 0

  years <- c(seq(1995, 2025, by = 5), 2050, 2100, 2150)
  price400 <- new.magpie(getRegions(base_prices), years, getNames(base_prices), fill = 0)
  price400[, "y2050", "co2_c"] <- 400
  price400[, "y2100", "co2_c"] <- 400
  price400[, "y2150", "co2_c"] <- 400
  price400 <- time_interpolate(price400, seq(1995, 2150, by = 5), TRUE)
  price400[, , "ch4"] <- price400[, , "co2_c"] * 28
  price400[, , "n2o_n_direct"] <- price400[, , "co2_c"] * 265 * 44 / 28
  price400[, , "n2o_n_indirect"] <- price400[, , "co2_c"] * 265 * 44 / 28
  price400[, , "co2_c"] <- price400[, , "co2_c"] * 44 / 12

  patched_prices <- mbind(
    add_dimension(base_prices, dim = 3.2, add = "scen", nm = "T0-GHG"),
    add_dimension(price400, dim = 3.2, add = "scen", nm = "T400-GHG")
  )

  patch_dir <- file.path("patch_inputdata", "global_ghg_prices")
  if (dir.exists(patch_dir)) {
    unlink(patch_dir, recursive = TRUE)
  }
  dir.create(patch_dir, recursive = TRUE)
  write.magpie(patched_prices, file.path(patch_dir, "f56_pollutant_prices.cs3"))
  tardir(patch_dir, file.path("patch_inputdata", "global_ghg_prices.tgz"))
  unlink(patch_dir, recursive = TRUE)
}

