run_global_scenario <- function(title, pollutant_prices, second_gen_biodem,
                                rewetting_switch) {
  cfg$title <- title
  cfg$gms$c56_pollutant_prices <- pollutant_prices
  cfg$gms$c56_pollutant_prices_noselect <- "T0-GHG"
  cfg$gms$c60_2ndgen_biodem <- second_gen_biodem
  cfg$gms$c60_2ndgen_biodem_noselect <- second_gen_biodem
  cfg$gms$s58_rewetting_switch <- rewetting_switch
  start_run(cfg, codeCheck = FALSE)
}

